import json
import random

def generate_10k_mega_data():
    # القوالب الأساسية لكل قسم (لضمان الجودة)
    sections = {
        "الترحيب": {
            "keywords": ["مرحبا", "اهلين", "هلا", "السلام عليكم", "يا هلا بغدير", "صباح الخير", "كيف حالك", "شو الاخبار"],
            "answers": ["يا هلا بمبرمجتنا المبدعة غدير!", "أهلاً بك! أنا بوتك الذكي IntelliGhadeer.", "وعليكم السلام، أنرتِ الشات!"]
        },
        "النصائح": {
            "keywords": ["انصحني", "نصيحة اليوم", "نصيحة برمجية", "كيف انجح", "نصيحة غدير", "تطوير الذات"],
            "answers": ["الاستمرار هو سر النجاح، لا تتوقفي أبداً!", "تعلمي شيئاً جديداً كل يوم، ولو كان بسيطاً.", "الخطأ في الكود هو أول خطوة لتعلم الحل الصحيح."]
        },
        "الذكاء_الاصطناعي": {
            "keywords": ["ذكاء اصطناعي", "AI", "تعلم الآلة", "مستقبل التقنية", "الروبوتات", "NLP"],
            "answers": ["الذكاء الاصطناعي هو محاكاة لعقل الإنسان ليحل المشكلات المعقدة.", "المستقبل للذكاء الاصطناعي، وأنتِ جزء من هذا المستقبل بمشاريعكِ."]
        },
        "البرمجة": {
            "keywords": ["بايثون", "فلاتر", "Flutter", "لغة دارت", "تطوير التطبيقات", "كود برمجى", "Debugging"],
            "answers": ["البرمجة ليست مجرد كود، بل هي فن حل المشكلات.", "فلاتر هو خيارك الأفضل لبناء تطبيقات مذهلة وسريعة."]
        },
        "أشياء_تطوير": {
            "keywords": ["تطوير المشروع", "تحسين الكود", "تنظيم الملفات", "واجهة المستخدم", "UX UI"],
            "answers": ["الاهتمام بتجربة المستخدم (UX) يجعل تطبيقكِ ناجحاً ومميزاً.", "نظمي كودكِ دائماً ليكون من السهل تطويره في المستقبل."]
        },
        "منوعات_وتقنية": {
            "keywords": ["تكنولوجيا", "انترنت الاشياء", "الاردوينو", "الحساسات", "ابتكار", "هندسة"],
            "answers": ["التقنية تتطور كل ثانية، ابقي دائماً على اطلاع.", "مشروعكِ في الأردوينو هو بداية رائعة في عالم الهندسة والابتكار."]
        }
    }

    final_data = {}
    
    # توزيع الـ 10,000 سؤال على الأقسام (تقريباً 1666 سؤال لكل قسم)
    count_per_section = 10000 // len(sections)

    for category, content in sections.items():
        final_data[category] = []
        for i in range(count_per_section):
            # اختيار كلمة عشوائية وإضافة لمسات عليها لتكون مختلفة
            keyword = random.choice(content["keywords"])
            prefixes = ["", "ممكن", "أحتاج", "ايش هو", "شرح", "تكلم عن", "معلومات عن"]
            suffixes = ["", "لو سمحت", "يا بوت", "بسرعة", "الآن", "بالتفصيل"]
            
            question = f"{random.choice(prefixes)} {keyword} {random.choice(suffixes)}".strip()
            
            final_data[category].append({
                "question": question,
                "answers": [random.choice(content["answers"])]
            })

    # حفظ الملف الضخم
    with open('qa_data.json', 'w', encoding='utf-8') as f:
        json.dump(final_data, f, ensure_ascii=False, indent=4)
    
    print(f"✅ تم الانتهاء! ملف qa_data.json يحتوي الآن على {len(sections)*count_per_section} سؤال وجواب جاهز.")

if __name__ == "__main__":
    generate_10k_mega_data()